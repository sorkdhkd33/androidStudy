package com.example.classstudy

class Operator {
    fun plus(a: Int, b: Int) : Int {
        return a+b
    }
    fun minus(a: Int, b: Int) : Int {
        return a-b
    }
    fun multi(a: Int, b: Int) : Int {
        return a*b
    }
    fun divi(a: Int, b: Int) : Int {
        return a/b
    }
}